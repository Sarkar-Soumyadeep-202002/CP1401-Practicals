choice=input("Steak Choice (rare,medium,well-done) ").lower()
if choice=='rare':
    print("Please wait for 2 minutes")
elif choice=='medium':
    print("Please wait for 4 minutes")
elif choice=='well-done':
    print("Please wait for 6 minutes")
elif choice=='burnt':
    print("Wait for 8 minutes")
else:
    print("Ain't no steak like that")


